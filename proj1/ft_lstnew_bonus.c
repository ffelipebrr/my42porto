#include "libft.h"

t_list	*ft_lstnew(void *data)
{
	t_list	*new_lst;

	new_lst = (t_list *)malloc(sizeof(t_list));
	if (!new_lst)
		return (NULL);
	new_lst -> content = data;
	new_lst -> next = NULL;
	return (new_lst);
}
